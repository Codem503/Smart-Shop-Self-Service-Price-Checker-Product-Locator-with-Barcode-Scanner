    private void save_and_exitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_save_and_exitActionPerformed

    String name = jn.getText(); 
    String details  = jd.getText();
    String barcode = jb.getText(); 
    String location = jl.getText();
    String price = jp.getText();
    String category = (String) jComboBox1.getSelectedItem();

    if (name.isEmpty() || details.isEmpty() || barcode.isEmpty() || location.isEmpty() || price.isEmpty() || selectedImageBytes == null) {
        JOptionPane.showMessageDialog(this, "Please fill in all fields and add an image!");
        return;
    }
    try {
        Double.parseDouble(price); // Validate price is a number
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Please enter a valid price!");
        return;
    }

    try {
        // Database connection setup
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/ProductScan", "root", "");
        String query = "INSERT INTO addproduct (name, details, Barcode, location, price, pic,category) VALUES (?,?, ?, ?, ?, ?, ?)";
        
        PreparedStatement pst = conn.prepareStatement(query);
        pst.setString(1, name);
        pst.setString(2, details);
        pst.setString(3, barcode);
        pst.setString(4, location);
        pst.setString(5, price);
        pst.setBytes(6, selectedImageBytes); // Save the image as a BLOB
        pst.setString(7, category);
        // Execute the insert
        pst.executeUpdate();

        // Inform the user
        JOptionPane.showMessageDialog(this, "Product added successfully!");

        // Close the connection
        pst.close();
        conn.close();

        // Close the window
        DisplayAddProduct pi = new DisplayAddProduct();
        pi.setVisible(true);
        this.dispose();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }


        
    }//GEN-LAST:event_save_and_exitActionPerformed

