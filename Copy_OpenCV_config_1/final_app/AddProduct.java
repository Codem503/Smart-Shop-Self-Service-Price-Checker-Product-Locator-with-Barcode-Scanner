    private void jLabel11MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel11MouseClicked
        // TODO add your handling code here:
        // File chooser for selecting images
        javax.swing.JFileChooser fileChooser = new javax.swing.JFileChooser();
        fileChooser.setDialogTitle("Select an Image");

        // Filter to only accept image files
        fileChooser.setFileFilter(new javax.swing.filechooser.FileNameExtensionFilter("Image Files", "jpg", "png", "jpeg", "gif"));

        // Show the dialog and capture the result
        int result = fileChooser.showOpenDialog(this);
        if (result == javax.swing.JFileChooser.APPROVE_OPTION) {
            // Get the selected file
            java.io.File selectedFile = fileChooser.getSelectedFile();

            try {
                // Convert the file into a byte array
                java.nio.file.Path path = selectedFile.toPath();
                byte[] imageBytes = java.nio.file.Files.readAllBytes(path);

                // Store the image bytes in a global variable for database saving
                selectedImageBytes = imageBytes;

                // Create an ImageIcon and scale the image to fit the panel
                javax.swing.ImageIcon imageIcon = new javax.swing.ImageIcon(imageBytes);
                java.awt.Image scaledImage = imageIcon.getImage().getScaledInstance(
                    pic1.getWidth(), pic1.getHeight(), java.awt.Image.SCALE_SMOOTH
                );

                // Add the image to a JLabel and set it in pic1
                javax.swing.JLabel imageLabel = new javax.swing.JLabel(new javax.swing.ImageIcon(scaledImage));
                imageLabel.setBounds(0, 0, pic1.getWidth(), pic1.getHeight());

                // Remove any existing components in pic1
                pic1.removeAll();

                // Add the imageLabel to pic1
                pic1.setLayout(null);
                pic1.add(imageLabel);

                // Refresh pic1 to display the new image
                pic1.revalidate();
                pic1.repaint();

            } catch (Exception e) {
                javax.swing.JOptionPane.showMessageDialog(this, "Error loading image: " + e.getMessage());
            }
        }
        System.out.println("LayeredPane dimensions: " + pic1.getWidth() + "x" + pic1.getHeight());
    }//GEN-LAST:event_jLabel11MouseClicked

